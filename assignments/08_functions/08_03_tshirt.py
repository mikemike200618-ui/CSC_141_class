print("a function called make_shirt().") 
def make_shirt(size, message):
    """Display a message about the shirt size and the printed message."""
    print(f"The shirt size is {size} and it has the message: '{message}' printed on it.") 
make_shirt('L', 'Hello, World!') 
print("call the function with different arguments:") 
make_shirt('M', 'Python is awesome!')
# This function takes a shirt size and a message as arguments and prints out the details of the shirt.
