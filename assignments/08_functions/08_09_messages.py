print(" a list containing a series of short messages.") 
def show_messages(messages):
    """Print each message in the list."""
    for message in messages:
        print(message) 
messages = ["Hello, world!", "Python is fun.", "Functions are useful."] 
show_messages(messages) 
# This function takes a list of messages as an argument and prints each message.
