# Make a list of the multiples of 3 from 3 to 30 and print the list
threes = list(range(3, 31, 3))
print(threes) 
# The range function starts at 3, goes up to (but does not include) 31, and increments by 3 to get only multiples of 3
# The list function converts the range object into a list
# Finally, the print function outputs the list of multiples of 3
