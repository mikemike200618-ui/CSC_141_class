# Use the range function to create a list of odd numbers from 1 to 20 and print the list
odd_numbers = list(range(1, 20, 2)) 
print(odd_numbers) 
# The range function starts at 1, goes up to (but does not include) 20, and increments by 2 to get only odd numbers
# The list function converts the range object into a list
# Finally, the print function outputs the list of odd numbers